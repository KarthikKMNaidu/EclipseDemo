package programs;

public class SwitchDemo {

	public static void main(String[] args) {
		
		String s = "A";
		
		switch(s){
		
		default: 
			System.out.println("hhd");
			
			
		case "A":
			
			System.out.println("a alpa");
		
		
		}
		
		
		for(;;) {
			
			System.out.println("Hi");
		}
		
	}
}
